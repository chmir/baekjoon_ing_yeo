//최대공약수(GCD)와 최소공배수(LCM) 문제
/*
GCD(최대공약수): 두 자연수의 공약수 중 가장 큰 값
LCM(최소공배수): 두 자연수의 공배수 중 가장 작은 양의 정수

[소인수분해 관점]
- LCM: 각 소인수의 "최대 지수"를 택해 곱한 값
  예) 12=2^2*3^1, 18=2^1*3^2 -> LCM=2^2*3^2=36
- GCD: 두 수가 공통으로 갖는 소인수만 곱한 값
  예) 12=2^2*3^1, 18=2^1*3^2 -> GCD=2^1*3^1=6

[유클리드 호제법(나머지 반복) 원리]
gcd(a, b) = gcd(b, a % b)
- q를 몫, r을 나머지라고 했을 때, 두 수 a,b에 대하여 다음과 같이 표현 가능하다. a = b*q+r, r = a-b*q
  a와 b를 둘 다 나누는 수 d는 r도 나누고, 반대로 b와 r을 나누는 d는 a도 나눈다.
(정리) d|a, d|b 이면 d|r 이고, d|b, d|r 이면 d|a 이다. 
 1) d|a, d|b 이면 r = a - b*q 이므로 d|r -> d는 (b,r)의 공약수 
 2) d|b, d|r 이면 a = b*q + r 이므로 d|a -> d는 (a,b)의 공약수 
- 따라서 (a,b)의 공약수들과 (b,r)의 공약수들은 서로 완전히 같다. 
 -> gcd(a,b) = gcd(b,r) = gcd(b, a%b) 
- 반복을 계속하면 (x,y) -> (y, x%y) 로 바뀌는데, 매 단계마다 공약수 집합이 같으므로 gcd 값은 변하지 않는다. 
  결국 어느 순간 나머지가 0이 되어 y=0이 된다. 즉 (x,0) 상태가 된다. 
  (x,0)의 공약수는 x의 약수들이고, 그 중 최대는 x이므로 gcd(x,0)=x이다.
  앞에서 gcd 값이 변하지 않았으니, 이 마지막 x가 처음 (a,b)의 gcd다.

[LCM 계산]
LCM(a,b) = a*b / GCD(a,b)
- g = gcd(a,b) 라고 두면, g는 a와 b의 공통인 약수 중 가장 큰 값이다.
  따라서 a와 b는 각각 a = g*x, b = g*y 형태로 쓸 수 있다. (x,y는 몫이며 정수)
- 이때 x와 y는 서로소이다. (g = gcd(a,b)일 때 x=a/g, y=b/g는 서로소)
  이유: 만약 x와 y가 어떤 공약수 d>1을 가진다면(즉 x와 y가 둘 다 d의 배수라면)
      x = d*x', y = d*y' 로 쓸 수 있고 (x', y'는 정수이며 d로 나눈 뒤의 몫)
      그러면 a = g*x = g*d*x', b = g*y = g*d*y' 가 되어
      g*d가 a와 b의 공약수가 된다.
      그런데 d>1이면 g*d는 g보다 더 큰 공약수이므로,
      g가 "최대"공약수라는 정의와 모순.
      -> 그래서 gcd(x,y)=1 (서로소)
- 이제 공배수(특히 최소공배수)를 만들 때는, 공통으로 들어갈 g와, 서로소(x,y)를 포함하면 된다.
  -> lcm = g * x * y
- 식으로 정리하면
  lcm = g*x*y = g*(a/g)*(b/g) = (a*b)/g = (a/g)*b
  이 문제의 테스트케이스로는 그럴일은 없지만, 오버플로 예방 차원에서 나눗셈을 먼저한다고 가정 
*/

//입력 (한 줄에 두 자연수)
int[] input = Array.ConvertAll(Console.ReadLine().Split(), int.Parse);
int a = input[0];
int b = input[1];
//연산
//유클리드 호제법: gcd(x, y) = gcd(y, x % y), y가 0이면 x가 gcd
int Gcd(int x, int y) 
{
    while (y != 0)
    {
        int r = x % y;
        x = y;
        y = r;
    }
    return x;
}
//GCD계산
int gcd = Gcd(a, b);
//LCM 계산 
int lcm = (a / gcd) * b;
//출력
Console.Write($"{gcd}\n{lcm}");
